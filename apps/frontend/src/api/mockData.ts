function buildPaperAccount({
  currentEquity = 10000,
  allocatedCapital,
  openRiskAmount,
  projectedBasePnl,
  projectedStretchPnl,
  projectedStopLoss,
}: {
  currentEquity?: number;
  allocatedCapital: number;
  openRiskAmount: number;
  projectedBasePnl: number;
  projectedStretchPnl: number;
  projectedStopLoss: number;
}) {
  return {
    account_size: 10000,
    current_equity: currentEquity,
    allocated_capital: allocatedCapital,
    open_risk_amount: openRiskAmount,
    projected_base_pnl: projectedBasePnl,
    projected_stretch_pnl: projectedStretchPnl,
    projected_stop_loss: projectedStopLoss,
    risk_pct_of_account: Number(((openRiskAmount / 10000) * 100).toFixed(2)),
    projected_reward_to_risk: projectedStopLoss > 0 ? Number((projectedBasePnl / projectedStopLoss).toFixed(2)) : 0,
  };
}

